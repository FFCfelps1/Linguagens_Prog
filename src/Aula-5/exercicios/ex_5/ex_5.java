public class ex_5 {
    public static void main(String[] args) {
        int var = 20;
        do {
            System.out.println("valor 1:    " + var);
            var -= 1;

        } while (var > 0);

        do {
            System.out.println("valor 1:    " + var);
            var += 1;
            
        } while (var < 21);
    }
}